namespace TAFitting.Data;

/// <summary>
/// Represents a SI prefix.
/// </summary>
internal enum SIPrefix
{
    /// <summary>
    /// No prefix.
    /// </summary>
    None = 0,

    /// <summary>
    /// Quetta (Q, 10^30)
    /// </summary>
    Quetta = 30,

    /// <summary>
    /// Ronna (R, 10^27)
    /// </summary>
    Ronna = 27,

    /// <summary>
    /// Yotta (Y, 10^24)
    /// </summary>
    Yotta = 24,

    /// <summary>
    /// Zetta (Z, 10^21)
    /// </summary>
    Zetta = 21,

    /// <summary>
    /// Exa (E, 10^18)
    /// </summary>
    Exa = 18,

    /// <summary>
    /// Peta (P, 10^15)
    /// </summary>
    Peta = 15,

    /// <summary>
    /// Tera (T, 10^12)
    /// </summary>
    Tera = 12,

    /// <summary>
    /// Giga (G, 10^9)
    /// </summary>
    Giga = 9,

    /// <summary>
    /// Mega (M, 10^6)
    /// </summary>
    Mega = 6,

    /// <summary>
    /// Kilo (k, 10^3)
    /// </summary>
    Kilo = 3,

    /// <summary>
    /// Hecto (h, 10^2)
    /// </summary>
    Hecto = 2,

    /// <summary>
    /// Deca (da, 10^1)
    /// </summary>
    Deca = 1,

    /// <summary>
    /// Deci (d, 10^-1)
    /// </summary>
    Deci = -1,

    /// <summary>
    /// Centi (c, 10^-2)
    /// </summary>
    Centi = -2,

    /// <summary>
    /// Milli (m, 10^-3)
    /// </summary>
    Milli = -3,

    /// <summary>
    /// Micro (μ, 10^-6)
    /// </summary>
    Micro = -6,

    /// <summary>
    /// Nano (n, 10^-9)
    /// </summary>
    Nano = -9,

    /// <summary>
    /// Pico (p, 10^-12)
    /// </summary>
    Pico = -12,

    /// <summary>
    /// Femto (f, 10^-15)
    /// </summary>
    Femto = -15,

    /// <summary>
    /// Atto (a, 10^-18)
    /// </summary>
    Atto = -18,

    /// <summary>
    /// Zepto (z, 10^-21)
    /// </summary>
    Zepto = -21,

    /// <summary>
    /// Yocto (y, 10^-24)
    /// </summary>
    Yocto = -24,

    /// <summary>
    /// Ronto (r, 10^-27)
    /// </summary>
    Ronto = -27,

    /// <summary>
    /// Quecto (q, 10^-30)
    /// </summary>
    Quecto = -30,
} // internal enum SIPrefix
