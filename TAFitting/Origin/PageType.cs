namespace TAFitting.Origin;

/// <summary>
/// Represents a page type in Origin.
/// </summary>
internal enum PageType
{
    /// <summary>
    /// A matrix.
    /// </summary>
    Matrix = 1,

    /// <summary>
    /// A worksheet.
    /// </summary>
    Worksheet = 2,

    /// <summary>
    /// A graph.
    /// </summary>
    Graph = 3,

    /// <summary>
    /// A layout.
    /// </summary>
    Layout = 4,

    /// <summary>
    /// Notes.
    /// </summary>
    Notes = 5,
} // internal enum PageType
