namespace TAFitting.Print;

/// <summary>
/// Represents the position of additional content in a printout.
/// </summary>
internal enum AdditionalContentPosition
{
    /// <summary>
    /// The additional content is placed in the upper-left corner.
    /// </summary>
    UpperLeft,

    /// <summary>
    /// The additional content is placed in the upper-right corner.
    /// </summary>
    UpperRight,

    /// <summary>
    /// The additional content is placed in the lower-left corner.
    /// </summary>
    LowerLeft,

    /// <summary>
    /// The additional content is placed in the lower-right corner.
    /// </summary>
    LowerRight,
} // internal enum AdditionalContentPosition
